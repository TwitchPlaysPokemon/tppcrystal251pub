Function1f8000: ; 1f8000 generatres mon and trainer and stores them in wram. full structure is, from BT_OTTrainer, 11 bytes of trainer data (name and class), 177 bytes for 3 mons and 36 bytes of ????
	ld a, [rSVBK] ; changes have removed the mons in "battle" and inputted directly into OTPartyMons instead
	push af
	ld a, $3
	ld [rSVBK], a
	xor a
	ld hl, BT_OTTrainer
	ld bc, $00e0
	call ByteFill ; fill 340 bytes with 0 from d100
	ld a, $ff
	ld [wd10c], a ; set 3 variables to ff
	ld [wd147], a
	ld [wd182], a
	ld de, BT_OTTrainer
	ld a, [hRandomAdd]
	ld b, a
.asm_1f8022
	call Random
	ld a, [hRandomAdd] ; load 2 random numbers and add them
	add b
	ld b, a
	and $7f
	cp (BattleTowerTrainersEnd - BattleTowerTrainers) / 11
	jr nc, .asm_1f8022 ; loop until good random number
	ld b, a
	ld a, $1
	call GetSRAMBank
	ld c, $7
	ld hl, sBTTrainers
.asm_1f803a
	ld a, [hli]
	cp b ; if b = be48, try again
	jr z, .asm_1f8022
	dec c
	jr nz, .asm_1f803a ; check 7 slots
	ld hl, sBTTrainers
	ld a, [sNrOfBeatenBattleTowerTrainers]
	ld c, a
	ld a, b
	ld b, 0
	add hl, bc ; fill slot $be46 with the random number
	ld [hl], a
	call CloseSRAM
	push af ; store the random number
	ld hl, BattleTowerTrainers ; list of trainer names and classes
	ld bc, 11
	call AddNTimes ; go down to selected trainer
	; ld bc, 11 no the copy is part of the next function
	; call CopyBytes ; copy the data into BT_OTTrainer
	call Function1f8081 ; set-up trainer with random mons
	pop af ; random number used for trainer
	ld hl, BattleTowerTrainerData
	ld bc, $0024
	call AddNTimes ; ?????
	ld bc, $0024
	ld de, BT_OTTrainerData
.asm_1f8070
	ld a, BANK(BattleTowerTrainerData)
	call GetFarByte
	ld [de], a ; load ??? into de
	inc hl
	inc de
	dec bc
	ld a, b
	or c
	jr nz, .asm_1f8070 ; loop 24 times
	pop af
	ld [rSVBK], a
	ret
; 1f8081

Function1f8081:
	ld a, [rSVBK]
	push af
	ld a, $1
	ld [rSVBK], a

	push hl
	ld hl, OTPartyData
	ld bc, OTPartyDataEnd - OTPartyData
	xor a
	call ByteFill ;fill OTparty with 0
	pop hl

	ld de, OTPlayerName
	call .CopyName ; load name from trainer data into OTPlayerName, and end with a @, doesn't move hl
	ld de, OTPartyMonOT
	ld a, [PartyCount]
.loop1
	push af
	call .CopyName ;fill OT data
	pop af
	dec a
	jr nz, .loop1

	ld bc, 10 ;go down to trainer class and enter it
	add hl, bc
	ld a, [hl]
	ld [OtherTrainerClass], a

	ld hl, wd002
	ld a, [PartyCount]
	ld [hli], a ;load part count into buffer
	inc a
	push hl ;store wd003 on the stack
	ld c, a
	ld b, 0
	ld a, -1
	call ByteFill ;fill wd003 with partycount+1
	pop hl
	ld a, [wd002]
	ld c, a
.loop2
	push bc ;c = partycount
	push hl ; = wd003
	ld hl, TempMon
	ld bc, $30
	xor a
	call ByteFill
	pop hl
.loop3
	call Random
; check removed since the amount of battle tower mons is now 256 exactly
;	cp NUM_BATTLE_TOWER_MONS ;loop until less then battle tower mons, put number into c
;	jr nc, .loop3
	ld c, a
	push hl ;end of tempmon
	ld hl, wd002
	ld b, [hl] ;b = partycount
	inc hl
.loop4
	ld a, [hli]
	cp -1
	jr z, .pop_continue ;check if the same as previous selected mons
	cp c
	jr z, .pop_resample
	dec b
	jr nz, .loop4
	jr .pop_continue

.pop_resample ;call another if same mon picked twice
	pop hl
	jr .loop3

.pop_continue
	pop hl ;hl = wd003
	ld a, c
	ld [hli], a ;enter random selection
	push hl ;hl = slot after current in random selection memory
	ld bc, 6
	ld hl, BattleTowerMons
	call AddNTimes ;go to correct slot in mondata
	ld de, TempMon
	ld bc, 6
	call CopyBytes ;copy into tempmon

	ld hl, TempMonLevel
	call .FindHighestPartyLevel
	ld [hl], a
	ld [CurPartyLevel], a
	callba Function50e47
	ld hl, TempMonExp
	ld a, [hMultiplicand]
	ld [hli], a
	ld a, [hMultiplicand + 1]
	ld [hli], a
	ld a, [hMultiplicand + 2]
	ld [hli], a
	ld de, EVENT_TOWER_HARD_MODE
	ld b, CHECK_FLAG
	call EventFlagAction
	ld a, c
	and a
	jr z, .EasyStatXPTable
	ld a, $ff
	ld d, a
	ld e, a
	jr .StatXpInde
.EasyStatXPTable
	ld a, [wcf64]
	ld e, a
	ld d, 0
	ld hl, .StatExps ;load in statxp based on rounds won
	add hl, de
	add hl, de
	ld a, [hli]
	ld d, [hl]
	ld e, a
.StatXpInde
	ld c, 5
	ld hl, TempMonStatExp
.loop5
	ld a, d
	ld [hli], a
	ld a, e
	ld [hli], a
	dec c
	jr nz, .loop5
	call Random
	and $33
	add $cc
	ld [hli], a
	call Random
	and $33
	add $cc
	ld [hl], a
	ld a, [TempMonSpecies]
	ld [CurSpecies], a
	call GetBaseData
	ld hl, TempMonStatExp - 1
	ld de, TempMonMaxHP
	ld b, 1
	predef CalcPkmnStats
	ld de, TempMonHP
	ld hl, TempMonMaxHP
	ld a, [hli]
	ld [de], a
	inc de
	ld a, [hl]
	ld [de], a
	ld hl, TempMonMoves
	ld de, TempMonPP
	predef FillPP ;enter mon PP
	ld hl, TempMonHappiness
	ld a, $ff
	ld [hl], a
	ld hl, OTPartyCount
	ld a, [hl]
	push af ;push partycount
	inc [hl]
	inc hl
	ld c, a
	ld b, 0
	add hl, bc ;load species into OTSpecies
	ld a, [TempMonSpecies]
	ld [wd265], a
	ld [hli], a

	ld [hl], -1
	ld hl, OTPartyMon1
	ld a, c
	ld c, $30
	call AddNTimes ;go down to correct party mon
	ld d, h
	ld e, l
	ld hl, TempMon
	ld bc, $30
	call CopyBytes ;enter tempmon into OTParty
	call GetPokemonName
	pop af
	ld hl, OTPartyMonNicknames
	ld bc, NAME_LENGTH ;load mon name into nickname
	call AddNTimes
	ld d, h
	ld e, l
	ld hl, StringBuffer1
	call .CopyName
	pop hl
	pop bc
	dec c
	jp nz, .loop2
	pop af
	ld [rSVBK], a
	ret


.CopyName ; load name from hl into de, and end with a @, then reset hl
	push hl
	ld bc, NAME_LENGTH - 1
	call CopyBytes
	ld a, "@"
	ld [de], a
	pop hl
	ret

.FindHighestPartyLevel
	push hl
	push de
	push bc
	ld a, [PartyCount]
	ld d, a
	ld hl, PartyMon1Level
	ld e, 0
	ld bc, $30
.level_loop
	ld a, [hl]
	cp e
	jr c, .no_update
	jr z, .no_update
	ld e, a
.no_update
	add hl, bc
	dec d
	jr nz, .level_loop
	ld a, e
	pop bc
	pop de
	pop hl
	ret

